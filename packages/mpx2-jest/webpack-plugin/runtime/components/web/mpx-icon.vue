<script>
  import getInnerListeners from './getInnerListeners'
  import { processSize } from './util'

  let defaultColor = {
    success: '#09BB07',
    'success_no_circle': '#09BB07',
    info: '#10AEFF',
    warn: '#F76260',
    waiting: '#10AEFF',
    cancel: '#F43530',
    download: '#09BB07',
    search: '#B2B2B2',
    clear: '#B2B2B2'
  }

  export default {
    name: 'mpx-icon',
    props: {
      type: String,
      color: String,
      size: {
        type: [Number, String],
        default: 23
      }
    },
    computed: {
      _size () {
        return processSize(this.size)
      }
    },
    render (createElement) {
      let type = this.type || 'success'
      const sizeStr = isNaN(+this._size) ? this._size : this._size + 'px'
      const color = this.color || defaultColor[type]
      let style = {
        color: color,
        fontSize: sizeStr
      }
      if (global.__style === 'v2') {
        switch (type) {
          case 'success':
          case 'info':
          case 'warn':
          case 'waiting':
          case 'search':
          case 'clear':
          case 'success_no_circle':
            type = type + '-v2'
            style = {
              color: color,
              width: sizeStr,
              height: sizeStr
            }
            defaultColor = {
              success: '#07c160',
              'success_no_circle': '#07c160',
              info: '#10AEFF',
              warn: '#F76260',
              waiting: '#10AEFF',
              cancel: '#F43530',
              download: '#13bf69',
              search: '#7d7979',
              clear: '#B2B2B2'
            }
        }
      }
      const data = {
        class: ['mpx-icon', `mpx-icon-${type}`],
        style,
        on: getInnerListeners(this)
      }
      return createElement('i', data, this.$slots.default)
    }
  }
</script>

